// ResolveConfigPath returns the path where init-config should write, given a CLI flag value.
func ResolveConfigPath(cliPath string) string {
	if cliPath != "" {
		return cliPath
	}

	// Check if any default path already has a file
	paths := searchPaths("")
	for _, p := range paths {
		if _, err := os.Stat(p); err == nil {
			return p
		}
	}

	// Default to XDG config home
	xdg := os.Getenv("XDG_CONFIG_HOME")
	if xdg == "" {
		home, _ := os.UserHomeDir()
		xdg = filepath.Join(home, ".config")
	}
	return filepath.Join(xdg, "anilistgen", "anilistgen.yaml")
}
